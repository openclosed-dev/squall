    private static final Map<String, StandardKeyword> KEYWORD_MAP;

    static {
        KEYWORD_MAP = Stream.of(values())
            .filter(v -> v != UNDEFINED)
            .collect(Collectors.toMap(StandardKeyword::canonicalName, Function.identity()));
    }

    // Keyword interface

    @Override
    public String canonicalName() {
        return name();
    }

    @Override
    public final StandardKeyword standard() {
        return this;
    }

    public static StandardKeyword lookUp(String name) {
        return KEYWORD_MAP.getOrDefault(name, UNDEFINED);
    }
